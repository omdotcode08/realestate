/**
 * Pure function to recalculate KPIs based on simulator sliders.
 */
export function revenueSimulatorFormula(agents, proPct, featuredAvg) {
  const baseSub = 1999;
  const proSub = 4999;
  const featuredFee = 999;
  const leadFee = 250;
  
  const proAgents = Math.floor(agents * (proPct / 100));
  const baseAgents = agents - proAgents;
  
  const subRevenueMonth = (baseAgents * baseSub) + (proAgents * proSub);
  const featuredRevenueMonth = agents * featuredAvg * featuredFee;
  
  // Assume each agent gets about 15 paid leads a month
  const leadRevenueMonth = agents * 15 * leadFee;
  
  const totalRevenueMonth = subRevenueMonth + featuredRevenueMonth + leadRevenueMonth;
  const totalArr = totalRevenueMonth * 12;
  
  // Mock growth
  const growthPct = Math.min((proPct * 0.5) + (featuredAvg * 2), 120).toFixed(1);
  
  return {
    arr: `₹${(totalArr / 10000000).toFixed(2)} Cr`,
    growth: `${growthPct}%`,
    monthlyRevenue: totalRevenueMonth,
    leadsGenerated: agents * 45 // 45 leads total per agent per month
  };
}
